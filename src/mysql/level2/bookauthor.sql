CREATE TABLE BOOK (
    BOOK_ID INTEGER NOT NULL,
    CATEGORY VARCHAR(255) NOT NULL,
    AUTHOR_ID INTEGER NOT NULL,
    PRICE INTEGER NOT NULL,
    PUBLISHED_DATE DATE NOT NULL,
    PRIMARY KEY (BOOK_ID)
);

CREATE TABLE AUTHOR (
    AUTHOR_ID INTEGER NOT NULL,
    AUTHOR_NAME VARCHAR(255) NOT NULL,
    PRIMARY KEY (AUTHOR_ID)
);

