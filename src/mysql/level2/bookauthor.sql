-- DATE_FORMAT 함수를 통해 Date 타입을 String으로 형식을 원하는 데로 바꿀 수 있다.
SELECT BOOK_ID, A.AUTHOR_NAME, DATE_FORMAT(PUBLISHED_DATE, '%Y-%m-%d') FROM BOOK
    left join AUTHOR A on BOOK.AUTHOR_ID = A.AUTHOR_ID
    where CATEGORY='경제'
    order by PUBLISHED_DATE;